def load(
    file_path: Path,
    *,
    limit: Optional[int] = None,
    dry_run: bool = False,
    keep_indexes: bool = False,
) -> int:
    log.info("load: file=%s limit=%s dry_run=%s keep_indexes=%s",
             file_path, limit, dry_run, keep_indexes)
    if not file_path.exists():
        raise SystemExit(f"input file does not exist: {file_path}")

    type_hist: collections.Counter[str] = collections.Counter()
    seen = 0
    inserted = 0
    t0 = time.time()

    if dry_run:
        # Just iterate and count.
        for item, _ns_map in iter_logitems(file_path):
            seen += 1
            if item.type:
                type_hist[item.type] += 1
            if seen % PROGRESS_EVERY == 0:
                log.info("dry-run: parsed %d logitems in %.1fs",
                         seen, time.time() - t0)
            if limit and seen >= limit:
                break
        log.info("DRY-RUN summary: %d logitems parsed in %.1fs",
                 seen, time.time() - t0)
        log.info("type histogram: %s", type_hist.most_common(10))
        return seen

    # Real load. Open one connection; do everything in it.
    with psycopg.connect(f"service={SERVICE}") as conn:
        with conn.cursor() as cur:
            for stmt in SESSION_TUNING_SQL:
                cur.execute(stmt)

        actors = ActorRegistry(conn)

        saved_index_creates: dict[str, str] = {}
        if not keep_indexes:
            saved_index_creates = drop_logging_indexes(conn)

        # TRUNCATE logging only; never actor.
        with conn.cursor() as cur:
            log.info("TRUNCATE %s", LOGGING_TABLE)
            cur.execute(f"TRUNCATE TABLE {LOGGING_TABLE}")
        conn.commit()

        log_batch: list[tuple] = []

        try:
            for item, ns_map in iter_logitems(file_path):
                seen += 1
                if item.type:
                    type_hist[item.type] += 1
                row = _logitem_to_row(item, ns_map, actors)
                if row is None:
                    continue
                log_batch.append(row)

                if len(log_batch) >= BATCH_SIZE:
                    _copy_logging_batch(conn, log_batch)
                    inserted += len(log_batch)
                    log_batch.clear()
                    conn.commit()

                if seen % PROGRESS_EVERY == 0:
                    elapsed = time.time() - t0
                    rate = seen / elapsed if elapsed > 0 else 0.0
                    log.info(
                        "progress: %d parsed, %d inserted, %.0f rows/s, %.1fs",
                        seen, inserted, rate, elapsed,
                    )

                if limit and seen >= limit:
                    break

            # Flush remainder.
            _copy_logging_batch(conn, log_batch)
            inserted += len(log_batch)
            log_batch.clear()
            conn.commit()
        except Exception:
            log.exception("load failed; rolling back current batch")
            conn.rollback()
            raise

        if not keep_indexes and saved_index_creates:
            recreate_logging_indexes(conn, saved_index_creates)

        with conn.cursor() as cur:
            log.info("ANALYZE %s, %s", LOGGING_TABLE, ACTOR_TABLE)
            cur.execute(f"ANALYZE {LOGGING_TABLE}")
            cur.execute(f"ANALYZE {ACTOR_TABLE}")
        conn.commit()

    elapsed = time.time() - t0
    log.info("DONE in %.1fs", elapsed)
    log.info("seen=%d inserted=%d", seen, inserted)
    log.info("top log_types: %s", type_hist.most_common(10))
    return inserted


# ---------------------------------------------------------------------------
# Self-test (no DB)
# ---------------------------------------------------------------------------

def self_test() -> int:
    """Exercise log_params parsing and a few edge cases. Returns exit code."""
    failures: list[str] = []

    # 1. JSON form (newer rows: nested array).
    json_raw = '[["edit=sysop"],{"7d": 0, "1y": 0}]'
    out = parse_log_params(json_raw)
    expected = [["edit=sysop"], {"7d": 0, "1y": 0}]
    if out != expected:
        failures.append(f"JSON parse: got {out!r}, want {expected!r}")

    # 2. JSON dict form (block log).
    json_dict = '{"5::duration": "indefinite", "6::flags": "nocreate"}'
    out = parse_log_params(json_dict)
    if out != {"5::duration": "indefinite", "6::flags": "nocreate"}:
        failures.append(f"JSON-dict parse: got {out!r}")

    # 3. PHP-serialized array (older rows).
    # PHP equiv: a:1:{i:0;s:10:"edit=sysop";}
    # phpserialize represents this as {0: b'edit=sysop'} — int keys preserved
    # rather than being collapsed to a list. We stringify the keys for jsonb.
    php_raw = 'a:1:{i:0;s:10:"edit=sysop";}'
    out = parse_log_params(php_raw)
    if out != {"0": "edit=sysop"}:
        failures.append(f"PHP parse simple: got {out!r}")

    # 4. PHP-serialized assoc array with bytes keys.
    # a:2:{s:5:"level";s:5:"sysop";s:6:"expiry";s:8:"infinity";}
    php_assoc = 'a:2:{s:5:"level";s:5:"sysop";s:6:"expiry";s:8:"infinity";}'
    out = parse_log_params(php_assoc)
    if out != {"level": "sysop", "expiry": "infinity"}:
        failures.append(f"PHP parse assoc: got {out!r}")

    # 5. Freeform / unparseable: stored as {"raw": ...}.
    free_raw = "edit=sysop\nmove=sysop"
    out = parse_log_params(free_raw)
    if out != {"raw": free_raw}:
        failures.append(f"freeform fallback: got {out!r}")

    # 6. None / empty.
    if parse_log_params(None) is not None:
        failures.append("None did not return None")
    if parse_log_params("") is not None:
        failures.append("empty string did not return None")
    if parse_log_params("   ") is not None:
        failures.append("blank string did not return None")

    # 7. JSON-encodability of every result.
    for raw in (json_raw, json_dict, php_raw, php_assoc, free_raw):
        try:
            json.dumps(parse_log_params(raw), ensure_ascii=False)
        except (TypeError, ValueError) as e:
            failures.append(f"non-JSON-encodable for {raw!r}: {e}")

    # 8. ActorRegistry with a fake DB cursor that emulates the IDENTITY +
    # ON CONFLICT semantics. The real DB hands out monotonically-increasing
    # ids and dedups via the unique constraint; we mirror that in-memory.

    class _FakeIdentityCur:
        """Cursor stub: handles INSERT ... RETURNING and SELECT against an
        in-memory store. Shared across all cursors created from the same
        connection."""

        def __init__(self, store):
            self._store = store
            self._last_row: Optional[tuple] = None

        def __enter__(self):
            return self

        def __exit__(self, *a):
            return False

        def execute(self, sql: str, params=()):
            sql_lc = sql.lower()
            if sql_lc.startswith("insert into"):
                actor_user, actor_name = params
                key = (actor_user, actor_name)
                if key in self._store["map"]:
                    # Conflict: ON CONFLICT DO NOTHING returns no row.
                    self._last_row = None
                else:
                    new_id = self._store["next_id"]
                    self._store["next_id"] += 1
                    self._store["map"][key] = new_id
                    self._last_row = (new_id,)
            elif sql_lc.startswith("select actor_id"):
                actor_user, actor_name = params
                key = (actor_user, actor_name)
                aid = self._store["map"].get(key)
                self._last_row = (aid,) if aid is not None else None
            else:
                self._last_row = None

        def fetchone(self):
            return self._last_row

    class _FakeIdentityConn:
        def __init__(self):
            self._store = {"map": {}, "next_id": 1}

        def cursor(self):
            return _FakeIdentityCur(self._store)

    reg = ActorRegistry(_FakeIdentityConn())
    a = reg.get_or_create(123, b"Alice")
    b = reg.get_or_create(123, b"Alice")  # dedup
    c = reg.get_or_create(None, b"127.0.0.1")  # anonymous IP
    d = reg.get_or_create(None, b"127.0.0.1")
    if a != b:
        failures.append(f"actor dedup: a={a} b={b}")
    if c != d:
        failures.append(f"anon actor dedup: c={c} d={d}")
    if a == c:
        failures.append("distinct keys collapsed to same actor_id")

    # 9. _logitem_to_row edge: deleted contributor + missing comment.
    item = ParsedLogItem(
        id=7, type="delete", action="delete",
        timestamp="2020-01-01T00:00:00Z",
        comment=None, comment_deleted=True,
        user_text=None, user_id=None, user_deleted=True,
        logtitle="User talk:Foo", params=None,
    )
    ns_map = {"user talk": 3}
    row = _logitem_to_row(item, ns_map, reg)
    if row is None:
        failures.append("deleted item produced None row")
    else:
        # log_actor (col 4) must be NULL
        if row[4] is not None:
            failures.append(f"deleted contributor not NULL: {row[4]!r}")
        # log_namespace decomposed correctly
        if row[6] != 3:
            failures.append(f"namespace decomposition: got {row[6]}")
        if row[7] != b"Foo":
            failures.append(f"title bytes: got {row[7]!r}")
        # log_comment (col 9) must be NULL
        if row[9] is not None:
            failures.append(f"deleted comment not NULL: {row[9]!r}")
        # log_deleted (col 11) bitmask = 6 (comment=2 | user=4)
        if row[11] != 6:
            failures.append(f"log_deleted bitmask: got {row[11]}, want 6")

    # 10. _logitem_to_row: missing log_page case + present contributor.
    item2 = ParsedLogItem(
        id=8, type="protect", action="protect",
        timestamp="2020-01-01T00:00:00Z",
        comment="x", comment_deleted=False,
        user_text="Alice", user_id=42, user_deleted=False,
        logtitle="Donald Trump",
        params='[["edit=sysop"],{"7d":0}]',
    )
    row2 = _logitem_to_row(item2, {}, reg)
    if row2 is None:
        failures.append("normal protect item produced None row")
    elif row2[8] is not None:
        failures.append(f"log_page should be NULL: got {row2[8]!r}")
    elif row2[4] is None:
        failures.append("log_actor should be set for live contributor")

    if failures:
        print("SELF-TEST FAILURES:", file=sys.stderr)
        for f in failures:
            print(f"  - {f}", file=sys.stderr)
        return 1
    print("self-test: all checks passed")
    return 0


