def _to_jsonable(obj: Any) -> Any:
    """Recursively coerce a phpserialize-decoded structure to JSON-safe types.

    phpserialize yields:
      * dicts keyed by either ints or bytes
      * lists of values
      * bytes (PHP strings)
      * ints / floats / bool / None

    Postgres jsonb requires:
      * dicts with str keys
      * everything else JSON-serializable
    """
    if isinstance(obj, dict):
        out: dict[str, Any] = {}
        for k, v in obj.items():
            if isinstance(k, bytes):
                # PHP strings: best-effort UTF-8, fall back to latin-1 to never raise.
                try:
                    sk = k.decode("utf-8")
                except UnicodeDecodeError:
                    sk = k.decode("latin-1")
            else:
                sk = str(k)
            out[sk] = _to_jsonable(v)
        return out
    if isinstance(obj, (list, tuple)):
        return [_to_jsonable(x) for x in obj]
    if isinstance(obj, bytes):
        try:
            return obj.decode("utf-8")
        except UnicodeDecodeError:
            return obj.decode("latin-1")
    if isinstance(obj, float) and not math.isfinite(obj):
        # JSON spec forbids Infinity/-Infinity/NaN; Postgres jsonb rejects them.
        # Preserve the information losslessly as a string sentinel.
        if math.isnan(obj):
            return "NaN"
        return "Infinity" if obj > 0 else "-Infinity"
    # int, finite float, bool, None, str — already jsonable.
    return obj


def parse_log_params(raw: Optional[str]) -> Optional[Any]:
    """Parse the MediaWiki log_params blob.

    Returns a JSON-serializable Python value, or None if `raw` is None/empty.

    Strategy: try JSON first (newer rows), then phpserialize (older rows),
    finally fall back to {"raw": raw} for the rare freeform/key=value style.
    """
    if raw is None:
        return None
    s = raw.strip()
    if not s:
        return None

    # 1. JSON — by far the most common in 2020+ rows.
    # Route through _to_jsonable too: Python's json.loads accepts the
    # non-standard tokens Infinity/-Infinity/NaN as floats; we'd then re-emit
    # them as Infinity to Postgres jsonb, which rejects them.
    try:
        return _to_jsonable(json.loads(s))
    except (json.JSONDecodeError, ValueError):
        pass

    # 2. PHP-serialized — common in pre-2018 rows.
    # phpserialize raises a few different exception types depending on the
    # malformation. Catch broadly; the freeform fallback below is a safe net.
    try:
        decoded = phpserialize.loads(s.encode("utf-8", errors="surrogateescape"))
        return _to_jsonable(decoded)
    except Exception:
        pass

    # 3. Freeform — preserve the original string for later inspection.
    return {"raw": raw}


# ---------------------------------------------------------------------------
# XML streaming
# ---------------------------------------------------------------------------
#
# We initially used `mwxml.Dump` for parsing, but on real enwiki data its
# User.from_element raises TypeError on `<contributor><id /></contributor>`
# (empty <id> element, which appears in the wild). Rather than monkey-patch
# mwxml, we roll a small iterparse-based parser. The schema is fixed and tiny
# (id, timestamp, contributor{username,id,ip}, comment, type, action,
# logtitle, params), so this stays small.

# MediaWiki export-0.11 namespace.
_MW_NS = "{http://www.mediawiki.org/xml/export-0.11/}"


@dataclass
class ParsedLogItem:
    id: Optional[int]
    type: Optional[str]
    action: Optional[str]
    timestamp: Optional[str]
    comment: Optional[str]
    comment_deleted: bool
    user_text: Optional[str]
    user_id: Optional[int]
    user_deleted: bool
    logtitle: Optional[str]
    params: Optional[str]


def open_xml_stream(path: Path) -> io.IOBase:
    """Return a binary file-like that ElementTree's iterparse can read."""
    if path.suffix == ".gz":
        return gzip.open(str(path), "rb")
    return open(path, "rb")


def _localname(tag: str) -> str:
    """Strip the {namespace} prefix from an ElementTree tag."""
    if tag.startswith("{"):
        return tag.rsplit("}", 1)[1]
    return tag


def _parse_namespaces(siteinfo_elem: ET.Element) -> dict[str, int]:
    """Build {prefix-lowercase: int_key} from <siteinfo>/<namespaces>.

    Used to split a logtitle like "User talk:Foo" into (3, "Foo"). We only
    look up by canonical/localized prefix; if the title doesn't match any
    known prefix we treat it as namespace 0 (Main).
    """
    ns_map: dict[str, int] = {}
    namespaces = siteinfo_elem.find(f"{_MW_NS}namespaces")
    if namespaces is None:
        return ns_map
    for n in namespaces.findall(f"{_MW_NS}namespace"):
        key = n.get("key")
        if key is None:
            continue
        try:
            key_int = int(key)
        except ValueError:
            continue
        prefix = (n.text or "").strip()
        if prefix:
            ns_map[prefix.lower()] = key_int
    return ns_map


def split_title(logtitle: str, ns_map: dict[str, int]) -> tuple[int, str]:
    """Decompose 'User talk:Foo' → (3, 'Foo'). Default to (0, full)."""
    if ":" in logtitle:
        prefix, _, rest = logtitle.partition(":")
        ns_id = ns_map.get(prefix.lower())
        if ns_id is not None:
            return ns_id, rest
    return 0, logtitle


def _text_or_none(elem: Optional[ET.Element]) -> Optional[str]:
    if elem is None:
        return None
    return elem.text  # may itself be None for <tag />


def _parse_contributor(c: ET.Element) -> tuple[
    Optional[str], Optional[int], bool
]:
    """Return (user_text, user_id, deleted)."""
    if c.get("deleted") is not None:
        return None, None, True
    username_e = c.find(f"{_MW_NS}username")
    ip_e = c.find(f"{_MW_NS}ip")
    id_e = c.find(f"{_MW_NS}id")
    user_text: Optional[str] = None
    user_id: Optional[int] = None
    if username_e is not None and username_e.text:
        user_text = username_e.text
    elif ip_e is not None and ip_e.text:
        user_text = ip_e.text
    if id_e is not None and id_e.text and id_e.text.strip():
        try:
            user_id = int(id_e.text)
        except ValueError:
            user_id = None
    return user_text, user_id, False


def iter_logitems(path: Path) -> Iterator[tuple[ParsedLogItem, dict[str, int]]]:
    """Yield (ParsedLogItem, ns_map) tuples.

    ns_map is built from <siteinfo> and reused across all items in the file.
    Yielded as a tuple so callers don't have to thread the namespace map
    out-of-band.

    Memory: ElementTree builds the tree as it parses. We clear each finished
    element *and* the root, so the <mediawiki> root never accumulates the
    ~50M emptied <logitem> shells — without root.clear() the process grows
    unboundedly and slows down badly over a multi-hour run.
    """
    f = open_xml_stream(path)
    ns_map: dict[str, int] = {}
    try:
        # 'start' events are used only to capture the root element; all real
        # work happens on 'end', when each child is fully populated.
        context = ET.iterparse(f, events=("start", "end"))
        _event, root = next(context)
        for event, elem in context:
            if event != "end":
                continue
            tag = _localname(elem.tag)
            if tag == "siteinfo":
                ns_map = _parse_namespaces(elem)
                elem.clear()
                root.clear()
                continue
            if tag != "logitem":
                # Children of <siteinfo>/<logitem> fire 'end' too; they are
                # freed when their parent is cleared below.
                continue

            # Parse this <logitem>.
            id_e = elem.find(f"{_MW_NS}id")
            ts_e = elem.find(f"{_MW_NS}timestamp")
            type_e = elem.find(f"{_MW_NS}type")
            action_e = elem.find(f"{_MW_NS}action")
            logtitle_e = elem.find(f"{_MW_NS}logtitle")
            params_e = elem.find(f"{_MW_NS}params")
            comment_e = elem.find(f"{_MW_NS}comment")
            contributor_e = elem.find(f"{_MW_NS}contributor")

            log_id: Optional[int] = None
            if id_e is not None and id_e.text and id_e.text.strip():
                try:
                    log_id = int(id_e.text)
                except ValueError:
                    log_id = None

            comment_deleted = False
            comment_text: Optional[str] = None
            if comment_e is not None:
                if comment_e.get("deleted") is not None:
                    comment_deleted = True
                else:
                    comment_text = comment_e.text

            user_text: Optional[str] = None
            user_id: Optional[int] = None
            user_deleted = False
            if contributor_e is not None:
                user_text, user_id, user_deleted = _parse_contributor(
                    contributor_e
                )

            item = ParsedLogItem(
                id=log_id,
                type=_text_or_none(type_e),
                action=_text_or_none(action_e),
                timestamp=_text_or_none(ts_e),
                comment=comment_text,
                comment_deleted=comment_deleted,
                user_text=user_text,
                user_id=user_id,
                user_deleted=user_deleted,
                logtitle=_text_or_none(logtitle_e),
                params=_text_or_none(params_e),
            )
            yield item, ns_map
            # Free memory: drop this logitem and any prior emptied shells.
            elem.clear()
            root.clear()
    finally:
        try:
            f.close()
        except Exception:
            pass


