ACTOR_CACHE_MAX = 1_000_000


class ActorRegistry:
    """DB-backed actor resolver with a bounded LRU.

    Key:    (actor_user_or_None, actor_name_bytes)
    Value:  actor_id (bigint allocated by the DB IDENTITY column)

    The schema declares ``actor_id GENERATED BY DEFAULT AS IDENTITY`` and
    ``UNIQUE NULLS NOT DISTINCT (actor_user, actor_name)``. We rely on both:
    INSERT ... ON CONFLICT DO NOTHING RETURNING actor_id either mints a fresh
    id or returns no row (because the constraint already holds a row); on
    conflict we SELECT the existing id back. The two loaders converge on the
    same actor_id for the same (actor_user, actor_name) regardless of order.

    The LRU is just a memory-bound cache to skip the round-trip when we see
    the same contributor again in this process. Cap defaults to 1M entries.
    """

    def __init__(self, conn: psycopg.Connection, max_size: int = ACTOR_CACHE_MAX):
        self._conn = conn
        self._max_size = max_size
        # OrderedDict for cheap LRU semantics.
        from collections import OrderedDict
        self._cache: "OrderedDict[tuple[Optional[int], bytes], int]" = OrderedDict()
        self._sel_sql = (
            f"SELECT actor_id FROM {ACTOR_TABLE} "
            "WHERE actor_user IS NOT DISTINCT FROM %s AND actor_name = %s"
        )
        self._ins_sql = (
            f"INSERT INTO {ACTOR_TABLE} (actor_user, actor_name) VALUES (%s, %s) "
            "ON CONFLICT (actor_user, actor_name) DO NOTHING "
            "RETURNING actor_id"
        )

    def get_or_create(
        self, actor_user: Optional[int], actor_name: bytes
    ) -> int:
        key = (actor_user, actor_name)
        cached = self._cache.get(key)
        if cached is not None:
            self._cache.move_to_end(key)
            return cached
        with self._conn.cursor() as cur:
            cur.execute(self._ins_sql, (actor_user, actor_name))
            row = cur.fetchone()
            if row is not None:
                actor_id = row[0]
            else:
                # Conflict: row already exists. SELECT it back.
                cur.execute(self._sel_sql, (actor_user, actor_name))
                got = cur.fetchone()
                if got is None:
                    # Should not happen given the unique constraint, but
                    # guard against a transient anomaly.
                    raise RuntimeError(
                        f"actor row vanished after ON CONFLICT for "
                        f"(actor_user={actor_user!r}, actor_name={actor_name!r})"
                    )
                actor_id = got[0]
        self._cache[key] = actor_id
        self._cache.move_to_end(key)
        if len(self._cache) > self._max_size:
            self._cache.popitem(last=False)
        return actor_id


