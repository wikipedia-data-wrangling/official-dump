#!/usr/bin/env python3
"""
load_logging_xml.py — load enwiki-<run>-pages-logging.xml.gz into wiki20260401.

Phase 1b of the data-collection plan. WMF stopped publishing `logging.sql.gz`,
so the equivalent content arrives as MediaWiki <logitem> elements inside the
recombined `pages-logging.xml.gz` (or its 27 splits). This script streams that
XML, normalizes `log_params` to JSON, synthesizes a row in `actor` for each
contributor it has not seen yet, and bulk-loads `logging` via psycopg COPY.

Connects via libpq service=wiki (port encoded in ~/.pg_service.conf).

Co-design with the revision loader: the `actor` table is shared. Both loaders
must deduplicate the same way and never TRUNCATE it. The actor table itself
holds a UNIQUE NULLS NOT DISTINCT constraint on (actor_user, actor_name) and
its actor_id is a GENERATED BY DEFAULT AS IDENTITY column, so we never
allocate ids ourselves. Contributors are resolved a batch at a time: cache
misses are COPYed into a session TEMP table, minted with a single
``INSERT ... SELECT ... ON CONFLICT DO NOTHING``, then read back (minted or
pre-existing) with one join. A bounded in-process LRU absorbs the admins that
recur constantly, so most batches touch the DB only for genuinely new actors.

Examples:
  python load_logging_xml.py
  python load_logging_xml.py --file <path-to.xml.gz>
  python load_logging_xml.py --file <path> --limit 50000
  python load_logging_xml.py --dry-run
  python load_logging_xml.py --keep-indexes
  python load_logging_xml.py --self-test           # in-script unit tests, no DB
"""

