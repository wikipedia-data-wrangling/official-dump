LOGGING_COLS = (
    "log_id",
    "log_type",
    "log_action",
    "log_timestamp",
    "log_actor",
    "log_actor_name",
    "log_namespace",
    "log_title",
    "log_page",
    "log_comment",
    "log_params",
    "log_deleted",
)

def _parse_ts(s: str) -> Optional[dt.datetime]:
    """Parse a MediaWiki ISO-8601 UTC timestamp ('2020-01-01T00:00:00Z')."""
    if not s:
        return None
    # Python's fromisoformat doesn't accept the trailing 'Z' until 3.11.
    if s.endswith("Z"):
        s = s[:-1] + "+00:00"
    try:
        return dt.datetime.fromisoformat(s)
    except ValueError:
        return None


def _logitem_to_row(
    item: ParsedLogItem,
    ns_map: dict[str, int],
    actors: ActorRegistry,
) -> Optional[tuple]:
    """Map a ParsedLogItem to a row tuple matching LOGGING_COLS.

    Returns None if the item is unusable (no id, no type, no timestamp).
    """
    if item.id is None or item.type is None or item.timestamp is None:
        return None
    ts = _parse_ts(item.timestamp)
    if ts is None:
        return None

    # type/action are NOT NULL in our schema. action defaults to type if missing
    # (some old rows have no <action>).
    log_type = item.type
    log_action = item.action or item.type

    # logtitle: decompose with the dump's namespace map.
    if item.logtitle:
        log_namespace, title_str = split_title(item.logtitle, ns_map)
    else:
        log_namespace, title_str = 0, ""
    log_title_bytes = title_str.encode("utf-8", errors="surrogateescape")

    # contributor → actor synthesis.
    actor_id: Optional[int]
    actor_name_bytes: Optional[bytes]
    if item.user_deleted or item.user_text is None:
        actor_id = None
        actor_name_bytes = None
    else:
        actor_name_bytes = item.user_text.encode(
            "utf-8", errors="surrogateescape"
        )
        actor_id = actors.get_or_create(item.user_id, actor_name_bytes)

    log_comment = None if item.comment_deleted else item.comment

    # log_deleted: mirror MW's bitmask. The XML only flags comment/user for
    # log items (no text-suppression flag), so we set bits 2 and 4 only.
    log_deleted = 0
    if item.comment_deleted:
        log_deleted |= 2
    if item.user_deleted:
        log_deleted |= 4

    log_params = parse_log_params(item.params)
    log_params_json = (
        json.dumps(log_params, ensure_ascii=False)
        if log_params is not None else None
    )

    return (
        int(item.id),
        log_type,
        log_action,
        ts,
        actor_id,
        actor_name_bytes,
        int(log_namespace),
        log_title_bytes,
        None,  # log_page — not in pages-logging.xml
        log_comment,
        log_params_json,
        log_deleted,
    )


def _copy_logging_batch(
    conn: psycopg.Connection, rows: list[tuple]
) -> None:
    """Bulk-load a batch of logging rows via text-format COPY.

    We use the text format (not binary) because jsonb in binary COPY needs
    a hand-built leading version byte; text format lets psycopg/Postgres
    handle the encoding for every column type uniformly.
    """
    if not rows:
        return
    with conn.cursor() as cur:
        with cur.copy(
            f"COPY {LOGGING_TABLE} ({', '.join(LOGGING_COLS)}) FROM STDIN"
        ) as cp:
            for r in rows:
                cp.write_row(r)


