ACTOR_CACHE_MAX = 1_000_000


class ActorRegistry:
    """DB-backed actor resolver with a bounded LRU, resolving a whole batch
    of contributors per call.

    Key:    (actor_user_or_None, actor_name_bytes)
    Value:  actor_id (bigint allocated by the DB IDENTITY column)

    The schema declares ``actor_id GENERATED BY DEFAULT AS IDENTITY`` and
    ``UNIQUE NULLS NOT DISTINCT (actor_user, actor_name)``. Per-row resolution
    cost one network round-trip per cache miss, which dominated runtime once
    the working set outgrew the cache (~19M distinct actors in the log, mostly
    one-shot ``newusers`` performers). ``resolve_batch`` instead COPYs all the
    cache misses into a session TEMP table and runs two set-based statements:
    an ``INSERT ... SELECT ... ON CONFLICT DO NOTHING`` to mint ids, then a
    join back to read every id (minted or pre-existing) at once. The two
    loaders still converge on the same actor_id for the same key regardless
    of order.

    The LRU skips the COPY for contributors already seen in this process
    (admins recur constantly). Cap defaults to 1M entries.
    """

    _TEMP_DDL = (
        "CREATE TEMP TABLE IF NOT EXISTS _actor_keys "
        "(actor_user bigint, actor_name bytea) ON COMMIT PRESERVE ROWS"
    )
    _INSERT_SQL = (
        f"INSERT INTO {ACTOR_TABLE} (actor_user, actor_name) "
        "SELECT actor_user, actor_name FROM _actor_keys "
        "ON CONFLICT (actor_user, actor_name) DO NOTHING"
    )
    _JOIN_SQL = (
        "SELECT a.actor_id, k.actor_user, k.actor_name "
        f"FROM _actor_keys k JOIN {ACTOR_TABLE} a "
        "ON a.actor_user IS NOT DISTINCT FROM k.actor_user "
        "AND a.actor_name = k.actor_name"
    )

    def __init__(self, conn: psycopg.Connection, max_size: int = ACTOR_CACHE_MAX):
        self._conn = conn
        self._max_size = max_size
        # OrderedDict for cheap LRU semantics.
        from collections import OrderedDict
        self._cache: "OrderedDict[tuple[Optional[int], bytes], int]" = OrderedDict()

    def resolve_batch(
        self, keys: Iterable[tuple[Optional[int], bytes]]
    ) -> dict[tuple[Optional[int], bytes], int]:
        """Resolve many (actor_user, actor_name) keys to actor_ids at once.

        Returns a dict mapping every distinct input key to its actor_id. Cache
        hits are served from the LRU; misses — however many — are resolved
        with a single COPY plus two set-based statements.
        """
        result: dict[tuple[Optional[int], bytes], int] = {}
        misses: list[tuple[Optional[int], bytes]] = []
        miss_seen: set[tuple[Optional[int], bytes]] = set()
        for key in keys:
            cached = self._cache.get(key)
            if cached is not None:
                self._cache.move_to_end(key)
                result[key] = cached
            elif key not in miss_seen:
                miss_seen.add(key)
                misses.append(key)

        if misses:
            with self._conn.cursor() as cur:
                cur.execute(self._TEMP_DDL)
                cur.execute("TRUNCATE _actor_keys")
                with cur.copy(
                    "COPY _actor_keys (actor_user, actor_name) FROM STDIN"
                ) as cp:
                    for k in misses:
                        cp.write_row(k)
                cur.execute(self._INSERT_SQL)
                cur.execute(self._JOIN_SQL)
                for actor_id, actor_user, actor_name in cur.fetchall():
                    key = (actor_user, bytes(actor_name))
                    result[key] = actor_id
                    self._cache[key] = actor_id
                    self._cache.move_to_end(key)
            while len(self._cache) > self._max_size:
                self._cache.popitem(last=False)
            for k in misses:
                if k not in result:
                    raise RuntimeError(
                        f"actor key unresolved after batch insert: {k!r}"
                    )
        return result


