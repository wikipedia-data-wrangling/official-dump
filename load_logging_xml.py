def parse_logging_index_statements(indexes_sql: Path) -> dict[str, str]:
    """Parse schema/indexes.sql and return {index_name: full CREATE statement}
    for each logging-table index we know about."""
    text = indexes_sql.read_text(encoding="utf-8")
    statements: dict[str, str] = {}
    # Split on ';' at end of lines — each CREATE INDEX is one statement.
    # Handle multi-line statements (everything up to the next ';').
    raw_stmts = [s.strip() for s in text.split(";") if s.strip()]
    for stmt in raw_stmts:
        # Strip leading psql meta lines (\set, comments) defensively.
        # Match "CREATE INDEX [IF NOT EXISTS] name".
        m = re.search(
            r"CREATE\s+INDEX\s+(?:IF\s+NOT\s+EXISTS\s+)?([A-Za-z0-9_]+)",
            stmt, flags=re.IGNORECASE,
        )
        if not m:
            continue
        name = m.group(1)
        if name in LOGGING_INDEX_NAMES:
            # Strip leading SQL comments so each statement re-runs cleanly.
            cleaned = re.sub(r"^\s*--[^\n]*\n", "", stmt, flags=re.MULTILINE)
            statements[name] = cleaned.strip() + ";"
    return statements


def drop_logging_indexes(conn: psycopg.Connection) -> dict[str, str]:
    """DROP every known logging index. Returns the saved CREATE statements.

    Idempotent: missing indexes are fine (DROP IF EXISTS).
    """
    creates = parse_logging_index_statements(INDEXES_SQL_PATH)
    missing = [n for n in LOGGING_INDEX_NAMES if n not in creates]
    if missing:
        log.warning(
            "indexes.sql parsed but these expected indexes were not found: %s",
            missing,
        )
    with conn.cursor() as cur:
        for name in LOGGING_INDEX_NAMES:
            log.info("dropping index %s", name)
            cur.execute(f"DROP INDEX IF EXISTS {name}")
    conn.commit()
    return creates


def recreate_logging_indexes(
    conn: psycopg.Connection, creates: dict[str, str]
) -> None:
    """Recreate the indexes we dropped, in the original order."""
    with conn.cursor() as cur:
        for name in LOGGING_INDEX_NAMES:
            stmt = creates.get(name)
            if not stmt:
                log.warning("no recreate statement for %s; skipping", name)
                continue
            log.info("recreating index %s", name)
            t0 = time.time()
            cur.execute(stmt)
            log.info("  -> %s built in %.1fs", name, time.time() - t0)
    conn.commit()


