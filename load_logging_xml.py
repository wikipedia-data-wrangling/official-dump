def main(argv: Optional[list[str]] = None) -> int:
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--file", type=Path, default=DEFAULT_FILE,
                   help=f"path to pages-logging XML[.gz] (default: {DEFAULT_FILE})")
    p.add_argument("--limit", type=int, default=None,
                   help="stop after N logitems (testing)")
    p.add_argument("--dry-run", action="store_true",
                   help="parse and count, but do not load")
    p.add_argument("--keep-indexes", action="store_true",
                   help="don't drop/recreate indexes around the load")
    p.add_argument("--self-test", action="store_true",
                   help="run in-script unit tests for log_params parsing and exit")
    p.add_argument("-v", "--verbose", action="store_true",
                   help="enable DEBUG-level logging")
    args = p.parse_args(argv)

    logging.basicConfig(
        level=logging.DEBUG if args.verbose else logging.INFO,
        format="%(asctime)s %(levelname)s %(name)s: %(message)s",
        datefmt="%H:%M:%S",
    )

    if args.self_test:
        return self_test()

    load(
        args.file,
        limit=args.limit,
        dry_run=args.dry_run,
        keep_indexes=args.keep_indexes,
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
