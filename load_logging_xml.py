def load(
    file_path: Path,
    *,
    limit: Optional[int] = None,
    dry_run: bool = False,
    keep_indexes: bool = False,
    resume: bool = False,
) -> int:
    log.info("load: file=%s limit=%s dry_run=%s keep_indexes=%s resume=%s",
             file_path, limit, dry_run, keep_indexes, resume)
    if not file_path.exists():
        raise SystemExit(f"input file does not exist: {file_path}")

    type_hist: collections.Counter[str] = collections.Counter()
    seen = 0
    inserted = 0
    t0 = time.time()

    if dry_run:
        # Just iterate and count.
        for item, _ns_map in iter_logitems(file_path):
            seen += 1
            if item.type:
                type_hist[item.type] += 1
            if seen % PROGRESS_EVERY == 0:
                log.info("dry-run: parsed %d logitems in %.1fs",
                         seen, time.time() - t0)
            if limit and seen >= limit:
                break
        log.info("DRY-RUN summary: %d logitems parsed in %.1fs",
                 seen, time.time() - t0)
        log.info("type histogram: %s", type_hist.most_common(10))
        return seen

    # Real load. Open one connection; do everything in it.
    with psycopg.connect(f"service={SERVICE}") as conn:
        with conn.cursor() as cur:
            for stmt in SESSION_TUNING_SQL:
                cur.execute(stmt)

        actors = ActorRegistry(conn)

        # Resume mode: don't TRUNCATE; pick up after the last committed log_id.
        # MediaWiki's pages-logging dump emits <logitem> elements ordered by
        # log_id ascending, so we can skip any item with log_id <= max(log_id).
        min_log_id: Optional[int] = None
        if resume:
            with conn.cursor() as cur:
                cur.execute(f"SELECT COALESCE(MAX(log_id), 0) FROM {LOGGING_TABLE}")
                row = cur.fetchone()
                max_log_id = int(row[0]) if row else 0
            if max_log_id == 0:
                log.info("--resume: %s is empty; nothing to resume from", LOGGING_TABLE)
            else:
                min_log_id = max_log_id + 1
                log.info("--resume: skipping items with log_id <= %d", max_log_id)

        saved_index_creates: dict[str, str] = {}
        if not keep_indexes:
            saved_index_creates = drop_logging_indexes(conn)

        if not resume:
            # TRUNCATE logging only; never actor.
            with conn.cursor() as cur:
                log.info("TRUNCATE %s", LOGGING_TABLE)
                cur.execute(f"TRUNCATE TABLE {LOGGING_TABLE}")
            conn.commit()
        else:
            log.info("resume: skipping TRUNCATE; existing rows preserved")

        item_batch: list[tuple[ParsedLogItem, dict[str, int]]] = []
        skipped_resume = 0

        def flush_batch() -> int:
            """Batch-resolve every contributor, build rows, COPY, commit.

            Returns the number of rows actually inserted (items with no
            id/type/timestamp map to None and are dropped).
            """
            if not item_batch:
                return 0
            actor_map = actors.resolve_batch(
                k for k in (_actor_key(it) for it, _ in item_batch)
                if k is not None
            )
            rows = []
            for it, ns in item_batch:
                row = _logitem_to_row(it, ns, actor_map)
                if row is not None:
                    rows.append(row)
            _copy_logging_batch(conn, rows)
            conn.commit()
            item_batch.clear()
            return len(rows)

        try:
            for item, ns_map in iter_logitems(file_path):
                seen += 1
                if item.type:
                    type_hist[item.type] += 1
                if min_log_id is not None and item.id is not None and int(item.id) < min_log_id:
                    skipped_resume += 1
                    if skipped_resume % 1_000_000 == 0:
                        log.info("resume: fast-forwarded past %d items", skipped_resume)
                    continue
                item_batch.append((item, ns_map))

                if len(item_batch) >= BATCH_SIZE:
                    inserted += flush_batch()

                if seen % PROGRESS_EVERY == 0:
                    elapsed = time.time() - t0
                    rate = seen / elapsed if elapsed > 0 else 0.0
                    log.info(
                        "progress: %d parsed, %d inserted, %.0f rows/s, %.1fs",
                        seen, inserted, rate, elapsed,
                    )

                if limit and seen >= limit:
                    break

            # Flush remainder.
            inserted += flush_batch()
        except Exception:
            log.exception("load failed; rolling back current batch")
            conn.rollback()
            raise

        if not keep_indexes and saved_index_creates:
            recreate_logging_indexes(conn, saved_index_creates)

        with conn.cursor() as cur:
            log.info("ANALYZE %s, %s", LOGGING_TABLE, ACTOR_TABLE)
            cur.execute(f"ANALYZE {LOGGING_TABLE}")
            cur.execute(f"ANALYZE {ACTOR_TABLE}")
        conn.commit()

    elapsed = time.time() - t0
    log.info("DONE in %.1fs", elapsed)
    log.info("seen=%d inserted=%d", seen, inserted)
    log.info("top log_types: %s", type_hist.most_common(10))
    return inserted


